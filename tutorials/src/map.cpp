Map::Map()
{
    dirt = TextureManager::loadTexture("assets/dirt");
    grass = TextureManager::loadTexture("assets/grass");
    water = TextureManager::loadTexture("assets/water");

    loadMap(level_1);
    source.x = source.y = 0;
    source.w = destination.w = 32;    
    source.h = destination.h = 32;

    destination.x = destination.y = 0;
}

Map::~Map()
{

}


void Map::loadMap(int array[20][25])
{
    for (int row = 0; row < 20; row++)
    {
        for (int column = 0; column < 25; column++)
        {
            map[row][column] = array[row][column];
        }
    }
}


void Map::drawMap()
{
    int type = 0;    
    for (int row = 0; row < 20; row++)
    {
        for (int column = 0; column < 25; column++)
        {
            type = map[row][column];

            destination.x = column * 32;
            destination.y = row * 32;

            switch (type)
            {
                case 0:
                    TextureManager::draw(water, source, destination);
                    break;
                case 1:
                    TextureManager::draw(grass, source, destination);
                    break;
                case 2:
                    TextureManager::draw(dirt, source, destination);
                    break;
                default:
                    break;
            }
        }
    }

}


